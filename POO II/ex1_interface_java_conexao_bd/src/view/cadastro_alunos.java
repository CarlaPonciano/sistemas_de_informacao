    private void jButtonCadastrarAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarAlunoActionPerformed
        if(jTextFieldCpf.getText().isEmpty() ||
                jTextFieldEmail.getText().isEmpty() ||
                jTextFieldMatricula.getText().isEmpty() ||
                jTextFieldNome.getText().isEmpty() ||
                jTextFieldTelefone.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha os campos para realizar o cadastro!", "", JOptionPane.INFORMATION_MESSAGE);
        }else{
            AlunoDomain aluno = new AlunoDomain();
            aluno.setCpf(jTextFieldMatricula.getText());
            aluno.setEmail(jTextFieldEmail.getText());
            aluno.setMatricula(jTextFieldMatricula.getText());
            aluno.setNome(jTextFieldNome.getText());
            aluno.setTelefone(jTextFieldTelefone.getText());
            
            cadastrarAluno cadastro_aluno = new cadastrarAluno();
            cadastro_aluno.cadastrarAluno(aluno);
        }
    }//GEN-LAST:event_jButtonCadastrarAlunoActionPerformed

