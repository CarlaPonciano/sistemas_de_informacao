    private void jButtonCadastrarProfessorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarProfessorActionPerformed
        if(jTextFieldCpfProfessor.getText().isEmpty() ||
                jTextFieldEmailProfessor.getText().isEmpty() ||
                jTextFieldNomeProfessor.getText().isEmpty() ||
                jTextFieldTelefoneProfessor.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha os campos para realizar o cadastro!", "", JOptionPane.INFORMATION_MESSAGE);
        }else{
            ProfessorDomain professor = new ProfessorDomain();
            professor.setCpf(jTextFieldCpfProfessor.getText());
            professor.setEmail(jTextFieldEmailProfessor.getText());
            professor.setNome(jTextFieldNomeProfessor.getText());
            professor.setTelefone(jTextFieldTelefoneProfessor.getText());

            ProfessorDAO aluno_persistencia = new ProfessorDAO();
            if(aluno_persistencia.cadastrarProfessor(professor))
                JOptionPane.showMessageDialog(null, "Professor cadastrado com sucesso!", "", JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar o professor!", "", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonCadastrarProfessorActionPerformed

